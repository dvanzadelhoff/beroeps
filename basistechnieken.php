		<h1>Bak eens een visje</h1>
		<p>
		Het bakken van vis is een verloren gegane kunst in de keuken van tegenwoordig. Terwijl wij Nederlanders echt een volkje van viseters zijn. En vis bakken is niet moeilijk. De vis wordt het lekkerste als je hem in zijn geheel bakt: met huid en graten. Dan blijft de smaak het beste behouden.<br>
		Het allerbelangrijkste: haal je vis bij de visboer. De visboer heeft altijd het meest verse aanbod. Bovendien kan de visboer je vertellen welke vis je op dit moment moet eten, dit verschilt namelijk per seizoen.<br>
		Net als bij vlees haal je de vis op tijd uit de koelkast, een half uur van tevoren. Dep de vis goed droog met keukenpapier. Bestrooi de vis licht met bloem en wat peter en zout. Dit moet natuurlijk geen dikke laag zijn, het teveel aan bloem klop je er voorzichtig af.<br>
		Laat de boter smelten in de pan, met een beetje olie erbij is ook mogelijk. Doe de vis in de pan zodra de boter schuimt. Beweeg de vis een beetje heen en weer in de pan om te voorkomen dat hij aanbakt.<br>
		Haal de vis uit de pan, gelijk klaar voor consumptie. Serveer er optioneel een lekkere vissaus bij, lees hier meer informatie over vissauzen.
