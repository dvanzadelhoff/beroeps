		<h1>Een lekker stukje vlees bakken</h1>
		<p>
		Bakken betekent dat je een product in een open pan verhit, met vet om aanbranden te voorkomen. Vlees is heel geschikt om te bakken. Hiervoor heb je een ruime pan nodig met een dikke, gelijkmatige bodem. Hierdoor wordt de pan gelijkmatig verwarmd. De pan moet een opstaande rand van een paar centimeter hebben zodat het vrijgekomen vocht niet gelijk verdampt.<br>
		<br>
		Stap 1: haal het vlees ruim van te voren uit de koelkast zodat het op kamertemperatuur kan komen. <br>
		Stap 2: Dep het vlees droog met eens stukje keukenrol. <br>
		Stap 3: Doe het vet in de pan en laat het goed heet worden. Wacht tot het schuim is weggetrokken. <br>
		Stap 4: In de tussentijd kun je het vlees kruiden, dit moet vlak voor het bakken gebeuren.<br>
		Stap 5: Doe het vlees in de pan en laat het goed dicht schroeien voor je het voor de eerste keer omdraait. Het omdraaien doe je met een spatel, niet met een vork. Door met een vork te prikken, gaan de sappen verloren. Daardoor kan het vlees droog worden.<br>
		Stap 6: Beweeg het stuk vlees geregeld in de pan heen en weer zodat het niet aanbakt. <br>
		Stap 7: Bak het vlees tot het gaar is.<br>
		Stap 8: Laat het vlees even rusten en maak met vocht een jus van de bakboter.
		</p>
