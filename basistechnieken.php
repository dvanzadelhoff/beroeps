		<h1>Het bereiden van groente</h1>
		<p>
		Alle groente kun je in principe rauw eten, met uitzondering van aardappels. Dit is natuurlijk heel gezond maar alleen maar rauwe groente eten, daar wordt niemand vrolijk van.<br>
		Gelukkig zijn er diverse manier om groente te bereiden. De voorbereiding is in principe steeds hetzelfde: de groente goed wassen onder stromend water, harde stukken of de uiteinden eraf snijden en eventueel in kleinere stukken snijden.
		Je kunt groente natuurlijk altijd koken, traditioneel in water met een beetje zout. Gemiddeld is 8 tot 10 minuten genoeg, dat heeft de groente nog een beetje bite. Vroeger werd groente veel langer gekookt, vooral koolsoorten. Dat is niet nodig.
		</p>
		<br>
		<h1>Variatie met de bereiding van groente</h1>
		<p>
		Er zijn nog meer manieren om groente te bereiden. Je kunt groenten blancheren, dat is een korte variant van koken. Je doet de groente een minuutje in al kokend water, daarna spoel je het gelijk af onder koud water zodat het kookproces wordt afgebroken. Dit is handig als je verse groente wilt invriezen.<br>
		Daarnaast kun je groenten bakken (een gesnipperd uitje met paprika bijvoorbeeld), grillen, roosteren of wokken. Met al deze methodes worden de groentes kort bewerkt zodat de smaak optimaal behouden blijft.
		</p>
